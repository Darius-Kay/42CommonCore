int	ft_nbrsize(long long int n)
{
	int	len;

	len = 1;
	if (n >= 10)
	{
		while (n >= 10)
		{
			n = n / 10;
			len++;
		}
	}
	else if (n <= -10)
	{
		while (n <= -10)
		{
			n = n / 10;
			len++;
		}
	}
	return (len);
}
